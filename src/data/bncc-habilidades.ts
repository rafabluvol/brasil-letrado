export const BNCC_HABILIDADES: AnoHabilidades[] = [ANO_1, ANO_2, ANO_3, ANO_4, ANO_5];

// ============================================================
// HELPER FUNCTIONS
// ============================================================

/** Get all materias for a given ano */
export function getMateriasPorAno(ano: string): MateriaBNCC[] {
  const found = BNCC_HABILIDADES.find((a) => a.ano === ano);
  return found?.materias || [];
}

/** Get habilidades for a given ano + materia */
export function getHabilidadesPorMateria(ano: string, materia: string): HabilidadeBNCC[] {
  const materias = getMateriasPorAno(ano);
  const found = materias.find((m) => m.materia === materia);
  return found?.habilidades || [];
}

/** Get a random label_alternativo for a habilidade */
export function getLabelAlternativo(hab: HabilidadeBNCC): string {
  const labels = [hab.label, ...hab.label_alternativo];
  return labels[Math.floor(Math.random() * labels.length)];
}

/** Get habilidade by code across all anos */
export function getHabilidadeByCodigo(codigo: string): HabilidadeBNCC | null {
  for (const ano of BNCC_HABILIDADES) {
    for (const materia of ano.materias) {
      const hab = materia.habilidades.find((h) => h.codigo === codigo);
      if (hab) return hab;
    }
  }
  return null;
}

/** Format habilidade for dropdown display */
export function formatHabilidadeDropdown(hab: HabilidadeBNCC): string {
  return `${hab.label} (${hab.codigo})`;
}

/** Get all unique exercise types for a habilidade */
export function getExerciciosParaHabilidade(codigo: string): string[] {
  const hab = getHabilidadeByCodigo(codigo);
  return hab?.exercicios_ideais || EXERCICIOS_INTERDISCIPLINAR;
}
